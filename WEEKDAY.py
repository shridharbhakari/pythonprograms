weekday=int(input("ENTER A WEEKDAY NUMBER : "))
if weekday == 1 :
    print(weekday,"IS SUNDAY")
elif weekday == 2 :
    print(weekday,"IS MONDAY")
elif weekday == 3 :
    print(weekday,"IS TUESDAY")
elif weekday == 4 :
    print(weekday,"IS WENSDAY")
elif weekday == 5 :
    print(weekday,"IS THURSDAY")
elif weekday == 6 :
    print(weekday,"IS FRIDAY")
elif weekday == 7 :
    print(weekday,"IS SATURDAY")
else:
    print("PLEASE ENTER VALID WEEKDAY NUMBER :!!")

