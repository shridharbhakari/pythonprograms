if CH == 'a' or CH == 'A' :
    print(CH,"IS VOWEL")
elif CH == 'e' or CH == 'E' :
    print(CH,"IS VOWEL")
elif CH == 'i' or CH == 'I' :
    print(CH,"IS VOWEL")
elif CH == 'o' or CH == 'O' :
    print(CH,"IS VOWEL")
elif CH == 'u' or CH == 'U' :
    print(CH,"IS VOWEL")
else:
    print(CH,"IS A CONSONANT :!!")
